def triangle(n):
	return (n*(n+1))//2

def sum_non_used(l):
	result = triangle(2**32-1)
	for elem in l:
		start, stop = elem
		result -= (triangle(stop) - triangle(start-1))	
	return result

def ip_to_decimal(s):
	l = s.split('.')
	n = sum(int(l[3-i])*256**i for i in xrange(4))
	return n

